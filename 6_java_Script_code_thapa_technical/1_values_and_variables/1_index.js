// Naming Variables: Rules And Best Practies

/*
1. The first character must be a letter or an underscore(_) or an doller($). You can't use a number the first character.

2. The rest of the variable name can include any letter, any number, or the underscore. Can't use any other characters, including spaces.

3. Variable names are case sensitive.

4. No limit to the length of the variable name.

5. You can't use one of JavaScript's reserved words as a variable name.

*/