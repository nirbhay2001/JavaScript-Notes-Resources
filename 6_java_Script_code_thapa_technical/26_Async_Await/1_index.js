/*

 Async/await
    1. There's a special syntax to work with promises in a more comfortable fashion, called "async/await".It's surprisingly easy to understand and use.
    2. The word "async" before a function means one simple thing: a function always returns a promise
    3. So the async keyword is added to function to tell them to return a promise rather than directly returning the value
    4. we can use await when calling any function that returns a promise, including web API functions.
    5. The keyword await makes JavaScript wait until that promise settles and returns its result.

*/