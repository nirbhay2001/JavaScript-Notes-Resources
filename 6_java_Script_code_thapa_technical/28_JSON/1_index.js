/*
 1. JSON stands for JavaScript Object Notation
 2. JSon is a format for storing and transporting data
 3. JSON is often used when data is sent from a server to a web page
 4. JSON is lightweight data interchange format
 5. JSON is language independent
 6. JSON is "self-describing" and easy to uderstand

*/


/*
 JSON Data Types
    In JSON, values must be one of the following data types:
    1. string
    2. number
    3. object(JSON Object)
    4. array
    5. boolean
    6. null




    JSON values cannot be one of the following data types:
    1. function
    2. date
    3. undefined
*/


/*
 JSON Syntax Rules
 1. Data is in name.value pairs
 2. Data is separated by commas
 3. JSON object are surrounded by curly braces {}.
 4. Curly braces hold objects
 5. Square brackets hold  arrays

*/

{
    employee : [
        {
            "name":"vinod",
            "lastname":"thapa",
            "age":24,
            "profession":"webdeveloper"
        }
    ]
}